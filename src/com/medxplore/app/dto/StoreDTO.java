package com.medxplore.app.dto;

public class StoreDTO {
	private String storeName;
	private String storeAddress;
	private String storeContactNo;
	private String storeMapLink;
	public String getStoreMapLink() {
		return storeMapLink;
	}
	public void setStoreMapLink(String storeMapLink) {
		this.storeMapLink = storeMapLink;
	}
	public String getStoreName() {
		return storeName;
	}
	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}
	public String getStoreAddress() {
		return storeAddress;
	}
	public void setStoreAddress(String storeAddress) {
		this.storeAddress = storeAddress;
	}
	public String getStoreContactNo() {
		return storeContactNo;
	}
	public void setStoreContactNo(String storeContactNo) {
		this.storeContactNo = storeContactNo;
	}
	
	
}
